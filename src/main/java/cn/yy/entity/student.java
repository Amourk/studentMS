package cn.yy.entity;


public class student {
	private String studentId;//学生学号
	
	private String studentName;//学生名字
	
    private String studentSex;//学生性别

    private String studentBirthday;//学生出生日期
    
    private String studentDepartment;//学生系别
    
    private String studentClass;//学生班级
    
    private String studentPlace;//学生籍贯
    
    private String studentInschool;//学生入学日期
    
    private String studentPassword;//学生密码

	public String getStudentId() {
		return studentId;
	}

	public void setStudentId(String studentId) {
		this.studentId = studentId;
	}

	public String getStudentName() {
		return studentName;
	}

	public void setStudentName(String studentName) {
		this.studentName = studentName;
	}

	public String getStudentSex() {
		return studentSex;
	}

	public void setStudentSex(String studentSex) {
		this.studentSex = studentSex;
	}

	public String getStudentBirthday() {
		return studentBirthday;
	}

	public void setStudentBirthday(String studentBirthday) {
		this.studentBirthday = studentBirthday;
	}

	public String getStudentDepartment() {
		return studentDepartment;
	}

	public void setStudentDepartment(String studentDepartment) {
		this.studentDepartment = studentDepartment;
	}

	public String getStudentClass() {
		return studentClass;
	}

	public void setStudentClass(String studentClass) {
		this.studentClass = studentClass;
	}

	public String getStudentPlace() {
		return studentPlace;
	}

	public void setStudentPlace(String studentPlace) {
		this.studentPlace = studentPlace;
	}

	public String getStudentInschool() {
		return studentInschool;
	}

	public void setStudentInschool(String studentInschool) {
		this.studentInschool = studentInschool;
	}

	public String getStudentPassword() {
		return studentPassword;
	}

	public void setStudentPassword(String studentPassword) {
		this.studentPassword = studentPassword;
	}
    
    

}
