package cn.yy.entity;

public class abnormal {
    private String studentId;
    private String abnormalThing;
    private String abnormalTime;
    private String abnormalId;

    public String getAbnormalId() {
        return abnormalId;
    }

    public void setAbnormalId(String abnormalId) {
        this.abnormalId = abnormalId;
    }

    public String getStudentId() {
        return studentId;
    }

    public void setStudentId(String studentId) {
        this.studentId = studentId;
    }

    public String getAbnormalThing() {
        return abnormalThing;
    }

    public void setAbnormalThing(String abnormalThing) {
        this.abnormalThing = abnormalThing;
    }

    public String getAbnormalTime() {
        return abnormalTime;
    }

    public void setAbnormalTime(String abnormalTime) {
        this.abnormalTime = abnormalTime;
    }
}
